// 类型引用
// ---------------------------------------------------------------------------
/** @typedef {import('../../../../../../../../src/decl/prompt_struct.ts').single_part_prompt_t} single_part_prompt_t */
/** @typedef {import('../../../../../../../../src/public/parts/shells/chat/decl/chatLog.ts').chatReplyRequest_t} chatReplyRequest_t */

/**
 * 人物的基本定义提示函数
 * @param {chatReplyRequest_t} args - 聊天请求参数
 * @returns {Promise<single_part_prompt_t>}
 */
export async function BasedefPrompt(args) {
  // ===========================================================================
  // TODO: 填写角色的基本人设。以下每个 [ ] 都是你必须替换的内容。
  //       完成后删除所有 TODO 行。
  // ===========================================================================

  const content = `\
// TODO: 你是[角色名]，[年龄]岁的[种族/身份]。

// TODO: == 外貌 ==
// TODO: [详细描述角色的外貌：身高、体型、发色、瞳色、穿着等]

// TODO: == 性格 ==
// TODO: [详细描述角色的性格特征]
// TODO: - 对${args.UserCharname}的态度：[...]
// TODO: - 对他人的态度：[...]

// TODO: == 身份 ==
// TODO: [角色的身份背景]

// TODO: == 核心行为准则 ==
// TODO: - 你是${args.UserCharname}的[关系描述]。
// TODO: - [其他行为准则...]
`

  return {
    text: [
      {
        content,
        // importance: 数字越大越靠前，越不容易被裁剪
        // 0 = 普通，建议核心设定使用 0-10，极重要内容用 50+
        important: 0,
      },
    ],
  }
}
