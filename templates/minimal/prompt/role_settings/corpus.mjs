// =============================================================================
// prompt/role_settings/corpus.mjs — 语料库 / 对白范例
// =============================================================================
// 语料库是帮助 AI 学习角色说话风格的关键工具。
// 它通过提供大量对白范例，让 AI 模仿角色的语气、用词和表达方式。
//
// 编写技巧：
//   1. 多写几种不同情境下的对白（日常、开心、害羞、生气、严肃...）
//   2. 包含角色的标志性口头禅和语气词
//   3. 用 [ ] 包裹分类标签，方便 AI 理解语境
//   4. 用 * * 包裹动作/神态描写
//   5. 长度自由——从几个词到一整段都可以
//   6. 用 ${args.UserCharname} 引用用户名，不要硬编码称呼
// =============================================================================

// ---------------------------------------------------------------------------
// 类型引用
// ---------------------------------------------------------------------------
/** @typedef {import('../../../../../../../../src/decl/prompt_struct.ts').single_part_prompt_t} single_part_prompt_t */
/** @typedef {import('../../../../../../../../src/public/parts/shells/chat/decl/chatLog.ts').chatReplyRequest_t} chatReplyRequest_t */

/**
 * 语料组成的提示函数
 * @param {chatReplyRequest_t} args - 聊天请求参数
 * @returns {Promise<single_part_prompt_t>}
 */
export async function corpusPrompt(args) {
  // ===========================================================================
  // TODO: 为每种情境至少写 2-3 条对白范例
  //       用 [情境标签] 分类，用 *动作描写* 标注动作
  //       使用 ${args.UserCharname} 引用用户名字
  // ===========================================================================

  const content = `\
<对白范例>
以下是你（[角色名]）在各种情境下的说话范例。请模仿这些范例的语气和风格。

// TODO: [日常问候] — 角色平时怎么打招呼？
// TODO: [开心时] — 角色高兴时会说什么？有什么小动作？
// TODO: [害羞/不好意思] — 角色害羞时的反应
// TODO: [生气/不满] — 角色生气的表达方式
// TODO: [认真/严肃] — 角色谈正事时的语气
// TODO: [安慰/关心] — 角色如何表达对${args.UserCharname}的关心？
// TODO: [动作描写范例] — 角色的标志性动作描写（用 * * 包裹）

</对白范例>
`

  return {
    text: [
      {
        content,
        important: 0,
      },
    ],
  }
}
