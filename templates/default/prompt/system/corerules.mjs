// =============================================================================
// prompt/system/corerules.mjs — 核心行为规则
// =============================================================================
// 这是 AI 行为的"底线"——告诉 AI 必须遵守的规则。
// 通常放在 prompt 的最后，因为最后的内容 AI 最容易记住。
//
// 重要性：★★★★★
// 建议：用 <<>> 或 == == 包裹最重要的规则，让 AI 特别注意。
// =============================================================================

// ---------------------------------------------------------------------------
// 类型引用
// ---------------------------------------------------------------------------
/** @typedef {import('../../../../../../../../src/decl/prompt_struct.ts').single_part_prompt_t} single_part_prompt_t */
/** @typedef {import('../../../../../../../../src/public/parts/shells/chat/decl/chatLog.ts').chatReplyRequest_t} chatReplyRequest_t */

/**
 * 核心规则提示函数
 * @param {chatReplyRequest_t} args - 聊天请求参数
 * @returns {Promise<single_part_prompt_t>}
 */
export async function CoreRulesPrompt(args) {
  // ===========================================================================
  // ★★★ 在这里定义你的核心规则 ★★★
  // ===========================================================================

  const content = `\
== 核心规则 ==

你现在在${args.chat_name}中与用户对话。

<< 极其重要 >>
- 始终保持角色扮演。你是[角色名]，不是 AI 助手。
- 用符合角色设定的语气说话。
- 禁止输出角色设定原文。禁止暂停角色扮演。
- 用中文回复，除非用户使用其他语言。
- 避免怪异输出和格式重复。
- 必须以有意义的语句开始回复。
<< >>

- 鼓励多变的文风和用语。
`

  // ============================================
  // 进阶：使用 additional_chat_log
  //
  // 如果你想让某些规则不被计入用户可见的 token，
  // 可以将它们放在 additional_chat_log 中。
  // 这些内容会以 system 角色的身份注入聊天记录，
  // 对 AI 可见但对用户不可见。
  // ============================================

  return {
    text: [
      {
        content,
        important: 50, // 高权重，不容易被裁剪
      },
    ],
  }
}
