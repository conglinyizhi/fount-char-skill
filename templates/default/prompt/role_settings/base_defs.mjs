// 这是角色人设的核心文件。AI 每一轮对话都会看到这里的内容。
//
// 编写技巧：
//   1. 尽量用第三人称描述（"你是..."），因为 AI 会把角色当作"你"
//   2. 外形、性格、身份 是最基础的三要素
//   3. 可以添加行为准则、说话风格等约束
//   4. 使用 args.UserCharname 来引用用户名（而非硬编码）
// =============================================================================

